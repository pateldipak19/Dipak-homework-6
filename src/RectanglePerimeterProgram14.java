public class RectanglePerimeterProgram14 {

    //Main method
    public static void main(String[] args) {

        //local variable declaration
       float width = 5.50F, height = 8.50F;
        float area, perimeter;

        area = width*height;//formula declaration
        perimeter = 2*(area);//formula declaration

        //print statements
        System.out.println("Area is 5.5 * 8.5 = "+area );
        System.out.println("Perimeter is 2 * (5.5 + 8.5) = "+perimeter);







        }






    }





